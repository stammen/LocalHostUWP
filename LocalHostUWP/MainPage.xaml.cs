namespace LocalHostUWP
{
    /// <summary>
    /// An empty page that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class MainPage : Page
    {
        public MainPage()
        {
            this.InitializeComponent();
        }

        private async void Button_Click(object sender, RoutedEventArgs e)
        {
            resultText.Text = "";
            Uri uri = new Uri("com.stammen.localhostwin32:?url=http://localhost:8000/test.json?foo=bar");
            var result = await Launcher.LaunchUriAsync(uri);
        }

        public void HandleProtocolActivation(ProtocolActivatedEventArgs args)
        {
            var uri = args.Uri;
            resultText.Text = Uri.UnescapeDataString(uri.Query);
        }
    }
